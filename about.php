<p><strong><B> NAMMA METRO</B></strong></p>
<br><br><br><p>  Namma Metro, also known as Bengaluru Metro is a metro system serving the city of Bengaluru, India. It is currently the second longest operational metro network in India after Delhi Metro.[4] On the other hand, Namma Metro ranked the 83rd largest metro system in terms of length and 79th largest metro network in terms of number of operating stations in the world. It also contains the first underground metro line in South India.The metro network consists of two colour-coded lines, with a total length of 42.3 kilometres serving 41 stations. The system has a mix of underground, at-grade, and elevated stations using standard-gauge tracks. The metro has an average daily ridership of 315,000 passengers.</p>

<p>
The Bangalore Metro Rail Corporation Ltd (BMRCL), a joint venture of the Government of India and the Government of Karnataka, built and operates the Namma Metro. Services operate daily between 05:00 and 23:00 running with a headway varying between 4–20 minutes. The trains are composed of three cars. The power output is supplied by 750 volt direct current through third rail. Namma Metro was the second rail transport system in India to use 750 V DC third rail traction, the first is Kolkata Metro.Construction
